package com.adr.dataclient.ui;

import java.awt.Component;
import javax.swing.DefaultListCellRenderer;
import javax.swing.Icon;
import javax.swing.ImageIcon;
import javax.swing.JList;

/**
 *
 * @author adrian
 */
public class ConnectionRenderer extends DefaultListCellRenderer {
    
    private static Icon connIcon = new ImageIcon(ConnectionRenderer.class.getResource("/com/adr/dataclient/ui/images/32x32/database.png"));

    @Override
    public Component getListCellRendererComponent(JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
        super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

        ClientConfig c = (ClientConfig) value;

        setText(c.getName());
        setIcon(connIcon);

        return this;
    }
}
